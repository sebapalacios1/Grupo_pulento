#include <stdio.h>

int main() {
    // Declaramos las variables con valores cualquiera
    int edad = 21;
    float nota = 6.5;
    char seccion = 'A';

    // Imprimimos usando %d para entero, %f para flotante y %c para caracter
    printf("Edad: %d\n", edad);
    printf("Nota del certamen: %f\n", nota);
    printf("Seccion: %c\n", seccion);

    return 0;
}
